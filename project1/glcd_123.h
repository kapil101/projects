void touchpad()	  	
{ unsigned int i,j,k=0;
address_left(0xb8,0x40);rs=1;   
for(i=0;i<8;i++){for(j=0;j<128;j++){
if(j>=64){address_right(0xb8+i,(0x40+j-64));rs=1; 
write_right(image1[k]);k++;}
else{address_left(0xb8+i,(0x40+j));   
rs=1; write_left(image1[k]);k++;}}} 
}    
